//: ## While циклы

/*:
 
    while condition {
        some code
    }
 
 */

var counter = 5

while counter > 0 {
    counter -= 1
    print(counter)
}

/*:
 
    repeat {
        some code
    } while condition
 
 */

counter

repeat {
    counter -= 1
    print(counter)
} while counter > 0

